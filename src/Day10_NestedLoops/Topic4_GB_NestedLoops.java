package Day10_NestedLoops;

public class Topic4_GB_NestedLoops {
    public static void main(String[] args) {
          int k=1;
        for (int i = 0; i <5; i++) {
            for (int j = 1; j <=i; j++) {
                {

                    System.out.print(j);
                    System.out.print("\t");
                    k++;

                }

            }
            System.out.println("");



        }
    }  }

