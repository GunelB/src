package Day13_Arrays;

public class Topic1_HA_ArrayIntro {
    public static void main(String[] args) {
        int Number1 = 5;
        int Number2 = 33;
        int Number3 = 1;
        int Number4 = 60;

        /*
        array
        DataType [] VariableName = {Value1, Value2, Value3.........} <- this is how we are defining array
         */

        int [] MyIntArray = {5, 33, 1, 60};

        String Name1 = "Henry";
        String Name2 = "John";

        // DataType [] Variablename = {values will be here}
        String [] MyStringArray = {"Henry", "John"};

        char Char1 = 'H';
        char Char2 = 'X';
        char Char3 = '3';

        // DataType [] VariableName = {my values will be here}

        char [] MyCharArray = {'H', 'X', '3'};

        boolean Bool1 = true;
        boolean Bool2 = false;
        boolean Bool3 = true;

        // DataType [] VariableName = {my values will be here};
        boolean [] MyBooleanArray = {true, false, true};// The last value doesn't have comma
    }
}
